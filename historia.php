            <h1>Historia da I.A</h1>
            <br>
            <br>
            <h2>Introdução</h2>
            <p>Existindo muito antes do que imaginamos, a história da inteligência artificial teve diversos protagonistas, todos com o mesmo intuito: desenvolver uma máquina que tivesse a capacidade humana de pensar e agir como tal. Um desses personagens foi Alan Turing, que descobriu um jeito de fazer seus testes com as máquinas. Seu intuito era realizar avaliações e saber se seria possível em uma conversa por escrito, a máquina se passar por um ser humano. Conhecido como o teste de Turing, esse foi um marco na história da inteligência artificial. Estima-se que a história da inteligência artificial se inicia após a Segunda Guerra Mundial, envolvendo diversos cientistas como Alan Turing e os pesquisadores Marvin Minsky, John McCarthy, Allen Newell e Herbert A. 
            </p>
            <p>Dando origem em 1956, a inteligência artificial foi investida com a criação de um campo para estudo da IA. Seu objetivo era desenvolver novas criações, logo a ciência começou a ser reconhecida quando formalizaram um termo na conferência de Dartmouth, com a missão de desenvolver máquinas inteligentes. Passando por análises e descobertas, em 1964 a I.A deu outro passo importante em sua história, quando criaram o primeiro chatbot do mundo. Com nome de ELIZA, sua função era realizar conversas de forma automática, comandada por dados e algoritmos que eram baseados em palavras-chave imitando uma psicanalista. Em busca da capacidade neurológica de um ser humano, a I.A foi capaz de criar máquinas desenvolvendo soluções inovadoras. 
            </p>
            <p>Porém, essa tecnologia teve sua definição conceitual nos idos dos anos 1950, na Universidade de Carnegie Mellon. Os cientistas Herbert Simon e Allen Newell foram os pais dessa ciência, criando, nessa universidade, o primeiro laboratório dedicado à inteligência artificial no âmbito acadêmico.
            </p>
            <br>
            <h2>I.A a partir das décadas</h2>
            <p>Ainda que os registros de Inteligência Artificial comecem a partir da década de 50, a verdade é que a IA começou bem antes disso.
            </p>
            <p>A sociedade vem buscando soluções para imitar a inteligência humana há mais de 2 mil anos. No período de 384 a 322 A.C, por exemplo, Aristóteles desenvolveu um sistema que através de premissas conseguia chegar em conclusões.
            </p>
            <p>Depois, com Leonardo da Vinci, que viveu entre 1452 e 1519, foi criada uma calculadora mecânica que foi o conceito inicial de automação da computação.
            </p>
            <br>
            <h3>Anos 50</h3>
            <p>Mas muito tempo depois, já na década de 50, no ano de 1956, que a já mencionada conferência foi realizada em Dartmouth aconteceu.
            </p>
            <p>Estes cientistas tinham interesse em redes neurais e estudo da inteligência, e passaram dois meses entendendo como descrever a inteligência humana para que máquinas pudessem imitá-la.
            </p>
            <p>Neste momento da história o interesse na IA era muito grande, e por isso os esforços realizados atraíram muitos investimentos.
            </p>
            <p>A partir destes investimentos foi possível criar a ARPA, Agência de Pesquisa de Projetos Avançados, focada em desenvolvimento de tecnologia. Foi nesse local inclusive que foi criada a internet.
            </p>
            <p>Em 1958 nasce a LISP, uma linguagem que virou padrão em sistemas de Inteligência Artificial. A partir da LISP, diversas outras linguagens foram originadas com o passar do tempo.
            </p>
            <p>Em 1959 o termo Machine Learning foi criado por Arthur Samuel, engenheiro do MIT. Neste momento, Machine Learning foi definido por ele como um campo de estudo que permite que os computadores aprendam sem ter uma programação específica para isso.
            </p>
            <br>
            <h3>Anos 70 e 80</h3>
            <p>Durante as décadas de 70 e 80 pouco avanço foi observado, sem muitas novidades para a área. Por isso, o período foi chamado de “inverno da Inteligência Artificial”.
            </p>
            <br>
            <h3>Anos 90</h3>
            <p>Nos anos 90 temos o lançamento do computador DEEP BLUE da IBM. Esta máquina foi capaz de vencer o campeão de xadrez soviético Garry Kasparov. Essa conquista aconteceu porque o computador era capaz de calcular possibilidades, prever respostas e sugerir movimentos mais adequados. Tudo porque existia um sistema de Inteligência Artificial instalado na máquina.
            </p>
            <br>
            <h3>Anos 2000</h3>
            <p>Diversos lançamentos foram realizados a partir de 2000, sempre melhorando a tecnologia já existente.
            </p>
            <p>Em 2002, a empresa iRobot lançou um assistente de limpeza autônomo, chamado Roomba. Hoje em dia muitas pessoas utilizam essa tecnologia para manter a casa limpa sem precisar passar panos ou outros produtos no chão.
            </p>
            <p>Em 2004, a Boston Dynamics lançou seu primeiro robô BIG DOG. Este primeiro lançamento está em desenvolvimento até hoje, sendo que os robôs podem ser vistos pelas ruas. Sua Inteligência Artificial é capaz de executar diversas tarefas simples e complexas. Além disso, os vídeos da empresa mostrando o desempenho do robô impressionam pela precisão dos movimentos.
            </p>
            <p>Já na metade dos anos 2000, em 2005, o primeiro carro autônomo foi lançado por ex-alunos da Universidade de Stanford, nos Estados Unidos. O carro foi chamado de Stanley, e a máquina conseguiu completar, de forma 100% autônoma, 212 km de trajeto em 6 horas e 53 minutos sem nenhuma batida.
            </p>
            <p>Então, em 2008, as gigantes de tecnologia Google e Apple lançaram seus recursos de reconhecimento de voz. Cada vez mais as pessoas têm acesso a essa tecnologia através de celulares e também assistentes pessoais em casa.
            </p>
